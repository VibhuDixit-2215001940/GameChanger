//Java is statically yped programming language
//Java has 8 primimtive data types 
/*PRIMITIVE DATA TYPES
 1.byte range(-128to127), takes 1 byte ,default value is 0
 2.short takes 2 byte,default value is 0
 3.int takes 4 bytes,default value is 0
 4.float takes 4 byte ,default value is 0.0f
 5.long takes 8 byte,default value is 0
 6.char takes 2 byte,default value is '\U000'
 7.double takes 8 bytes ,default value is 0.0d
 8.boolean size depends on JVM ,default value is false
 */
public class VariablesInJava{
    public static void main(String[] args){
        int a = 10;
        float x = 10.0f;
        char y = 'V';
        System.out.println(a);
        System.out.println(x);
        System.out.println(y);
    }
}